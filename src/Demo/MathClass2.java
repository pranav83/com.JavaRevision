package Demo;

public class MathClass2 {
//  increment operator, post increment operator and pre increment operator
//    Assignment Operator and how to use Assignment Operator;
public static void main(String[] args) {
    int x = 10;
    x++;
    System.out.println(x);
    System.out.println(x++);
    System.out.println(x);
    System.out.println(++x);
//    x++ is called pre increment and ++x is called post increment
    int y = 10;
    y += 5; // y = 10 + 5
    System.out.println(y);
//    = is called assignment operator
}
}
